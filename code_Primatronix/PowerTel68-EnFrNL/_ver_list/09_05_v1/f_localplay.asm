	SFR	4
	ANDK	0X0F
	BS	ACZ,LOCAL_PROTWR0	;for initial
	SBHK	1
	BS	ACZ,LOCAL_PROTWR1	;for record/line
	SBHK	1
	BS	ACZ,LOCAL_PROTWR_EXIT	;for end(TimeOut/BTONE/CTONE/VOX_ON)

	RET

