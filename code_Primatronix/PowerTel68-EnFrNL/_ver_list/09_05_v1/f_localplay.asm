	LAC	PRO_VAR
	ANDL	0XFF0F
	SAH	PRO_VAR

	CALL	CLR_TIMER

	LAC	CONF
	ANDL	~(1<<8)
	CALL	DAM_BIOSFUNC

	;LAC	MSG_ID
	LAC	FILE_ID
	CALL	LED_HLDISP

	RET
;---------------------------------------
LOCAL_PROPLY_2:			;等待REW松开
	LAC	MSG
	XORL	CMSG_TMR		;TMR
	BS	ACZ,LOCAL_PROPLY_REWEXE
	LAC	MSG
	XORL	CMSG_KEY5S		;repeat
	BS	ACZ,LOCAL_PROPLY_REP
	
	RET
LOCAL_PROPLY_REP:
