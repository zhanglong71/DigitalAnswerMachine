	XORL	CMSG_TMR		;time 1s
	BS	ACZ,LOCAL_PROTWR1_TMR
;LOCAL_PROTWR1_1:
	LAC	MSG
	XORL	CMSG_KEY4S		;MEMO stop record
	BS	ACZ,LOCAL_PROTWR1_STOP
;LOCAL_PROTWR1_2:
	LAC	MSG
	XORL	CREC_FULL		;REC_FULL
	BS	ACZ,LOCAL_PROTWR1_FUL
;LOCAL_PROTWR1_3:	
	LAC	MSG
	XORL	CRING_IN		;RING
	BS	ACZ,LOCAL_PROTWR1_STOP
;LOCAL_PROTWR1_4:	
	LAC	MSG
	XORL	CMSG_HSOFF		;并机
	BS	ACZ,LOCAL_PROTWR1_STOP

