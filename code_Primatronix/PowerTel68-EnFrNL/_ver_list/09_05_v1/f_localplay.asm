	BS	B1,LOCAL_PROPLY_OVER

;---------------------------------------
LOCAL_PROPLY_REW:
	LAC	PRO_VAR
	ANDL	0XFF0F
	ORK	0X020
	SAH	PRO_VAR

	LACK	0
	SAH	PRO_VAR1
	LACL	1000
	CALL	SET_TIMER

	;LAC	CONF
	;ORL	1<<8
	;CALL	DAM_BIOSFUNC

	CALL	INIT_DAM_FUNC
	
	RET
LOCAL_PROPLY_REW1:
	
	RET	
;---------------------------------------
LOCAL_PROPLY_PAUSE:
	LAC	CONF
	ANDL	0XF000
	SBHL	0X2000
	BS	ACZ,LOCAL_PROPLY_PAUSE0		;0X2000(message)
	SBHL	0X9000
	BS	ACZ,LOCAL_PROPLY_PAUSE0		;0XB000(voice prompt)
	BS	B1,LOCAL_PROPLY_PAUSE1
LOCAL_PROPLY_PAUSE0:
	LACL	DISP_PA		;"PA"
	SAH	LED_L
	
	LAC	CONF
	ORL	1<<8
	CALL	DAM_BIOSFUNC
	
	LACL	320
	CALL	PAUBEEP		;BEEP Prompt
	
	CALL	DAA_OFF
	
	LAC	PRO_VAR
	ANDL	0XFF0F
	ORK	0X010
	SAH	PRO_VAR
	
	LACK	0
	SAH	PRO_VAR1
	LACL	1000
	CALL	SET_TIMER

	RET
LOCAL_PROPLY_PAUSE1:		;若是BEEP,则循环再发
	LACL	CMSG_KEY1D
	CALL	STOR_MSG

	RET

LOCAL_PROX_VOLS:
	LACL	CMSG_VOLS
	CALL	STOR_MSG
	
	RET

LOCAL_PROX_VOLA:
	LACL	CMSG_VOLA
	CALL	STOR_MSG
	
	RET
;---------------------------------------
LOCAL_PROPLY_ERASE:		;删除后放下一条
	CALL	INIT_DAM_FUNC
	LAC	MSG_ID
	CALL	SET_DELMARK

	CALL	DAA_SPK
	LACK	0X025
	CALL	STOR_VP
	
	LACL	0X0101
	SAH	PRO_VAR
	
	LACL	DISP_dE
	SAH	LED_L		;Display "dE"

	;CALL	BEEP
	
	RET
;---------------------------------------
LOCAL_PROPLY_OVER:
	CALL	INIT_DAM_FUNC

	LAC	MSG_ID
	SBH	MSG_T
	BZ	SGN,LOCAL_PROPLY_STOP

	LAC	MSG_ID			;next message
	ADHK	1
	SAH	MSG_ID
	
	LAC	FILE_ID			;next message
	ADHK	1
	SAH	FILE_ID
	
	BIT	ANN_FG,12
	BZ	TB,LOCAL_PROPLY_LOADVP

	CALL	FFW_MANAGE
	BZ	ACZ,LOCAL_PROPLY_STOP	;THE LAST ONE	

LOCAL_PROPLY_LOADVP:

	LAC	FILE_ID
	CALL	LED_HLDISP
	CALL	BEEP

	LAC	MSG_ID
	ORL	0XFE00
	CALL	STOR_VP
	
	LAC	MSG_ID		;Check timer set or not
	ORL	0XA500
	CALL	DAM_BIOSFUNC
	BS	ACZ,LOCAL_PROPLY_LOADVP_END
	
	CALL	MSG_WEEK
	CALL	MSG_HOUR
	CALL	MSG_MIN
	CALL	MSG_AMPM
LOCAL_PROPLY_LOADVP_END:
	
	RET
;-------------------------------------------------------------------------------
LOCAL_PROPLY_STOP:		;全播放完毕
	CALL	INIT_DAM_FUNC
	CALL	DAA_SPK
	CALL	BBEEP
	
	CALL	CLR_TIMER

	LACL	0X0201
	SAH	PRO_VAR

	CALL	REAL_DEL
	CALL	GC_CHK
	
	CALL	VPMSG_CHK

	BIT	ANN_FG,12
	BS	TB,LOCAL_PROPLY_STOP1
	
	LAC	MSG_T
	CALL	LED_HLDISP
	
	RET
LOCAL_PROPLY_STOP1:
	LAC	MSG_N
	CALL	LED_HLDISP
	
	RET
;---------------------------------------
LOCAL_PROPLY_RING:
	CALL	INIT_DAM_FUNC
	CALL	DAA_OFF
	LACK	0X005
	CALL	STOR_VP
	
	CALL	PORT_HSPLYL
	CALL	PORT_PLYL
	
	CALL	CLR_TIMER
	
	LACK	0X0
	SAH	PRO_VAR

	CALL	REAL_DEL
	CALL	GC_CHK
	
	CALL	VPMSG_CHK

	BIT	ANN_FG,12
	BS	TB,LOCAL_PROPLY_RING1
	
	LAC	MSG_T
	CALL	LED_HLDISP
	
	RET
LOCAL_PROPLY_RING1:
	LAC	MSG_N
	CALL	LED_HLDISP
	
	RET
