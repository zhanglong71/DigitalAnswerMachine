LOCAL_PROREC:			;0Xyyy2 = record MEMO
	LAC	MSG
	XORL	CMSG_HSOFF
	BS	ACZ,LOCAL_PROREC_PMINE

	LAC	PRO_VAR
	SFR	4
	ANDK	0X0F
	BS	ACZ,LOCAL_PROREC0	;(0xyy02)prompt
	SBHK	1
	BS	ACZ,LOCAL_PROREC1	;(0xyy12)record
	SBHK	1
	BS	ACZ,LOCAL_PROREC2	;(0xyy22)Beep/Memful VP
	
	RET
;---------------------------------------
LOCAL_PROREC_PMINE:
	LACL	CRING_IN
	CALL	STOR_MSG	;当来铃一样的处理
	
	RET
;-------------------------------------------------------------------------------
LOCAL_PROREC0:
	LAC	MSG
	XORL	CMSG_TMR2
	BS	ACZ,LOCAL_PROREC_2TMR
	
	LAC	MSG
	XORL	CMSG_KEY4S		;MEMO key released worn and stop
	BS	ACZ,LOCAL_PROREC0_WORN
;LOCAL_PROREC0_1:
	LAC	MSG
	XORL	CVP_STOP
	BS	ACZ,LOCAL_PROREC0_RECSTART	;end beep and start record
;LOCAL_PROREC0_2:	
	LAC	MSG
	XORL	CRING_IN		;RING
	BS	ACZ,LOCAL_PROREC2_RING
	
	RET
;---------------------------------------
LOCAL_PROREC0_WORN:
	CALL	INIT_DAM_FUNC
	CALL	DAA_SPK
	CALL	BBBEEP

	;CALL	PORT_HSPLYL
	;CALL	PORT_PLYL
	;CALL	CLR_TIMER
	;CALL	CLR_TIMER2
	
	LACK	0X022
	SAH	PRO_VAR
	
	RET

;---------------------------------------
LOCAL_PROREC0_RECSTART:
	CALL	INIT_DAM_FUNC
	CALL	DAA_REC

	LACL	CMODE9|1<<7	;set TailCutUnit = 200ms
	CALL	DAM_BIOSFUNC

	CALL	REC_START
	
	CALL	PORT_HSPLYL
	CALL	PORT_PLYL

	LACL	1000
	CALL	SET_TIMER
	LACK	0
	SAH	PRO_VAR1

	LACK	0X012
	SAH	PRO_VAR
	
	RET
;-------------------------------------------------------------------------------
LOCAL_PROREC1:
	LAC	MSG
	XORL	CMSG_KEY4S		;stop record
	BS	ACZ,LOCAL_PROREC_STOP
LOCAL_PROREC1_1:
	LAC	MSG
	XORL	CREC_FULL		;timer
	BS	ACZ,LOCAL_PROREC_MFUL
LOCAL_PROREC1_2:
	LAC	MSG
	XORL	CMSG_TMR		;timer
	BS	ACZ,LOCAL_PROREC1_TMR
LOCAL_PROREC1_3:
	LAC	MSG
	XORL	CRING_IN		;RING
	BS	ACZ,LOCAL_PROREC1_RING
	RET
;---------------------------------------
LOCAL_PROREC1_TMR:
	LAC	PRO_VAR1
	ADHK	1
	SAH	PRO_VAR1
	
	RET

;---------------------------------------
LOCAL_PROREC_MFUL:
	CALL	INIT_DAM_FUNC
	CALL	DAA_SPK
	CALL	BBBEEP
	CALL	VP_MemoryFull
	CALL	HANDSET_CHK
	LACL	DISP_FL		;"FL"
	SAH	LED_L

	LACK	0X022
	SAH	PRO_VAR

	RET
;---------------------------------------
LOCAL_PROREC_STOP:
	LAC	PRO_VAR1
	SBHK	2
	BS	SGN,LOCAL_PRORECFAIL_STOP
	
	CALL	INIT_DAM_FUNC
	CALL	DAA_SPK
	CALL	BEEP
	CALL	HANDSET_CHK
	;CALL	PORT_HSPLYL
	;CALL	PORT_PLYL
	;CALL	CLR_TIMER
	;CALL	CLR_TIMER2
	
	LACK	0X22
	SAH	PRO_VAR
	
	CALL	VPMSG_CHK
	BIT	ANN_FG,12
	BS	TB,LOCAL_PROREC_STOP1
	LAC	MSG_T
	CALL	LED_HLDISP	;VP总数

	RET			;录音完毕后(正常)退出
LOCAL_PROREC_STOP1:
	LAC	MSG_N
	CALL	LED_HLDISP	;VP总数
	
	RET
;---------------------------------------
LOCAL_PROREC1_RING:

	LAC	PRO_VAR1
	SBHK	2
	BS	SGN,LOCAL_PROREC1_RING_RECFAIL
	
	CALL	INIT_DAM_FUNC
	CALL	DAA_OFF
	LACK	0X005
	CALL	STOR_VP

	CALL	PORT_HSPLYL
	CALL	PORT_PLYL
	CALL	CLR_TIMER
	CALL	CLR_TIMER2
	
	LACK	0X0
	SAH	PRO_VAR
	
	CALL	VPMSG_CHK
	BIT	ANN_FG,12
	BS	TB,LOCAL_PROREC_RING1
	LAC	MSG_T
	CALL	LED_HLDISP	;VP总数

	RET			;录音完毕后(正常)退出
LOCAL_PROREC_RING1:
	LAC	MSG_N
	CALL	LED_HLDISP	;VP总数
	
	RET
;---------------------------------------
LOCAL_PROREC1_RING_RECFAIL:	;录音失败(检到ring退出)
	LAC	CONF
	ORL	1<<11
	CALL	DAM_BIOSFUNC
	CALL	INIT_DAM_FUNC
	CALL	DAA_OFF
	LACK	0X005
	CALL	STOR_VP
	
	CALL	PORT_HSPLYL
	CALL	PORT_PLYL
	CALL	CLR_TIMER
	CALL	CLR_TIMER2
	
	LACK	0
	SAH	PRO_VAR
	
	RET
;---------------------------------------
LOCAL_PRORECFAIL_STOP:		;录音失败(正常退出)
	LAC	CONF
	ORL	1<<11
	CALL	DAM_BIOSFUNC
	CALL	INIT_DAM_FUNC
	CALL	DAA_SPK
	CALL	BBBEEP
	CALL	HANDSET_CHK
	;CALL	PORT_HSPLYL
	;CALL	PORT_PLYL
	;CALL	CLR_TIMER
	;CALL	CLR_TIMER2
	
	LACK	0X22
	SAH	PRO_VAR
	
	RET
;-------------------------------------------------------------------------------
LOCAL_PROREC2:			;timer over|full了,等待松手
	LAC	MSG
	XORL	CMSG_TMR2
	BS	ACZ,LOCAL_PROREC_2TMR
	
	LAC	MSG
	XORL	CVP_STOP
	BS	ACZ,LOCAL_PROREC2_VPSTOP
;LOCAL_PROREC2_1:
	LAC	MSG
	XORL	CMSG_KEY4S
	BS	ACZ,LOCAL_PROREC2_STOP	;Release "Rec" KEY (MemFul)
;LOCAL_PROREC2_2:	
	LAC	MSG
	XORL	CRING_IN		;RING
	BS	ACZ,LOCAL_PROREC2_RING

	RET
;---------------------------------------
LOCAL_PROREC2_VPSTOP:		;End of VOP
LOCAL_PROREC2_STOP:
LOCAL_PROREC2_RING:

	CALL	INIT_DAM_FUNC
	CALL	DAA_OFF
	LACK	0X005
	CALL	STOR_VP

	CALL	PORT_HSPLYL
	CALL	PORT_PLYL
	CALL	CLR_TIMER
	CALL	CLR_TIMER2

	LACK	0X0
	SAH	PRO_VAR
	
	RET

;-------------------------------------------------------------------------------
LOCAL_PROPLY:			;0Xyyy1
	LAC	MSG
	XORL	CMSG_KEY8D		;VOL-
	BS	ACZ,LOCAL_PROX_VOLS
	LAC	MSG
	XORL	CMSG_KEY7D		;VOL+
	BS	ACZ,LOCAL_PROX_VOLA
	LAC	MSG
	XORL	CMSG_KEY6S		;ON/OFF(stop)
	BS	ACZ,LOCAL_PROPLY_STOP
	LAC	MSG
	XORL	CRING_IN		;ON/OFF(stop)
	BS	ACZ,LOCAL_PROPLY_RING
	LAC	MSG
	XORL	CMSG_HSOFF
	BS	ACZ,LOCAL_PROPLY_RING	;HandSet off base cradle
	LAC	MSG
	XORL	CMSG_TMR2
	BS	ACZ,LOCAL_PROPLY_2TMR

	LAC	PRO_VAR
	SFR	4
	ANDK	0X0F
	BS	ACZ,LOCAL_PROPLY_0	;(0X0y01)playing
	SBHK	1
	BS	ACZ,LOCAL_PROPLY_1	;(0X0y11)pauseing
	SBHK	1
	BS	ACZ,LOCAL_PROPLY_2	;(0X0y21)pauseing(waiting for REW key release)
	
	RET
;-------------------------------------------------------------------------------
LOCAL_PROPLY_0:	
	LAC	MSG
	XORL	CMSG_KEY1D		;pause
	BS	ACZ,LOCAL_PROPLY_PAUSE

	LAC	PRO_VAR
	SFR	8
	ANDK	0X0F
	BS	ACZ,LOCAL_PROPLY_0_0	;(0X0001)playing Vop
	SBHK	1
	BS	ACZ,LOCAL_PROPLY_0_1	;(0X0101)playing Message
	SBHK	1
	BS	ACZ,LOCAL_PROPLY_0_2	;(0X0101)EndOfMessage/BBEEP
	
	RET
;-------------------------------------------------------------------------------
LOCAL_PROPLY_0_0:
	LAC	MSG
	XORL	CVP_STOP		;PLAY END
	BS	ACZ,LOCAL_PROPLY_0_OVER
	
	RET
;---------------------------------------
LOCAL_PROPLY_0_OVER:
	LACL	0X0101
	SAH	PRO_VAR
	BS	B1,LOCAL_PROPLY_OVER
;---------------------------------------
LOCAL_PROREC_2TMR:
LOCAL_PROOGM_2TMR:
LOCAL_PROPLY_2TMR:
LOCAL_PROERA_2TMR:
	LIPK	8
	IN	SYSTMP0,GPBD
	BIT	SYSTMP0,12
	BS	TB,LOCAL_PROPLY_0_2_VPSTOP	;查到SPK,立即退出
	BIT	SYSTMP0,5			;查到HS ,
	BS	TB,LOCAL_PROPLY_2TMR_CHKHS	;是挂机状态
	
	RET
;---------
LOCAL_PROPLY_2TMR_CHKHS:
	CALL	PORT_HSPLYL
	CALL	PORT_PLYH
	
	RET
;-------------------------------------------------------------------------------
LOCAL_PROPLY_0_1:	
	
LOCAL_PROPLY_0_1_1:
	LAC	MSG
	XORL	CMSG_KEY2D		;FFW
	BS	ACZ,LOCAL_PROPLY_FFW
LOCAL_PROPLY_0_1_2:
	LAC	MSG
	XORL	CMSG_KEY3D		;erase
	BS	ACZ,LOCAL_PROPLY_ERASE
