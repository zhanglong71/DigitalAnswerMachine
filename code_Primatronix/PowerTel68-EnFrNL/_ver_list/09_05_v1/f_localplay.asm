LOCAL_PROTWR0_INIT:			;开始录音	
	CALL	INIT_DAM_FUNC
	CALL	DAA_TWAY_REC
	CALL	TWHOOK_ON

	LACK	0X015
	SAH	PRO_VAR
	
	LACK	0
	SAH	PRO_VAR1
	LACL	1000
	CALL	SET_TIMER

	;CALL	MSG_CHK
	;CALL	GET_FILEID
	;CALL	SET_USRDAT
	
	CALL	REC_START
	
	RET
