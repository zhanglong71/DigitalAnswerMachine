	BS	ACZ,LOCAL_PROPLY_1_PLAY
LOCAL_PROPLY_1_2:
	;LAC	MSG
	;XORL	CMSG_KEY3D		;erase
	;BS	ACZ,LOCAL_PROPLY_ERASE
