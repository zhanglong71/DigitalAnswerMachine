	LAC	PRO_VAR
	ANDL	0XFF0F
	SAH	PRO_VAR
	
	BS	B1,LOCAL_PROPLY_LOADVP
;---------------------------------------
LOCAL_PROPLY_REWEXE:	
	LAC	MSG_ID
	SBHK	1
	BS	ACZ,LOCAL_PROPLY_REWEXE_1	;第一个吗?

	LAC	MSG_ID
	SBHK	1
	SAH	MSG_ID
	
	LAC	FILE_ID
	SBHK	1
	BS	ACZ,LOCAL_PROPLY_REWEXE_1
	SAH	FILE_ID
LOCAL_PROPLY_REWEXE_1:	
	BIT	ANN_FG,12
	BZ	TB,LOCAL_PROPLY_REWEXE_2

	CALL	REW_MANAGE
LOCAL_PROPLY_REWEXE_2:
	CALL	INIT_DAM_FUNC
	
	LAC	PRO_VAR
	ANDL	0XFF0F
	SAH	PRO_VAR
	
	CALL	CLR_TIMER
	BS	B1,LOCAL_PROPLY_LOADVP
;-------------------------------------------------------------------------------
LOCAL_PROPLY_0_2:
	LAC	MSG
	XORL	CVP_STOP		;PLAY END
	BS	ACZ,LOCAL_PROPLY_0_2_VPSTOP

	RET
;---------------------------------------	
LOCAL_PROPLY_0_2_VPSTOP:	;全播放完毕或强行退出Play-Mode
	CALL	INIT_DAM_FUNC
	CALL	DAA_OFF
	LACK	0X005
	CALL	STOR_VP

	CALL	CLR_TIMER
	CALL	CLR_TIMER2
	
	CALL	PORT_HSPLYL
	CALL	PORT_PLYL

	LACK	0X0
	SAH	PRO_VAR

