	CALL	VPMSG_CHK
	
	BS	B1,LOCAL_PROTWR1_STOP
	;BIT	ANN_FG,13
	;BS	TB,LOCAL_PROTWR1_FUL_EXIT
	;CALL	REC_START

	;RET
