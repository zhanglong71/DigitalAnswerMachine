	LAC	PHONE_ATT
	ANDK	0X07
   	;LACL	0XC603
   	ORL	0XC600
   	CALL	DAM_BIOSFUNC
;---Set LINE _GAIN & SPK_GAIN
	LAC	VOI_ATT			;Set SPK_GAIN
	SFR	4
	ANDK	0X07
	ADHL	PHOVOL_TAB
	CALL    GetOneConst
	SAH	MSG_N
	
	LAC	VOI_ATT			;Set LINE _GAIN
	SFR	8
	ANDK	0X07
	ADHL	PHOVOL_TAB
	CALL    GetOneConst
	SFL	4
	OR	MSG_N
	CALL	SET_PHOVOL		;0XC8XX(default Value = 0XC8AA)

