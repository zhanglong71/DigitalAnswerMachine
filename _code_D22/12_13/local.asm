	LAC	PHONE_ATT
	SFR	4
	ANDK	0X07
	;LACL	0XC903			;default Value
	ORL	0XC900
	CALL	DAM_BIOSFUNC
;---Line mute
    	LACL    0xC040         		;wait line mute for 400 ms
    	CALL    DAM_BIOSFUNC
;---free run
    	CALL	PHONE_START
.else
	CALL	SET_PHONERUN
.endif	
