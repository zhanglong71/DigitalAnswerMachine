;-------------------------------------------------------------------------------
;       Function : SEND_RECSTART
;
;       Input  : ACCH
;       Output : no
;-------------------------------------------------------------------------------
SEND_RECSTART:
	SAH	SYSTMP0
;---
	LACK	0X29
	CALL	SEND_DAT
	LAC	SYSTMP0
	CALL	SEND_DAT
	
	RET

